func TestParserRejectsNoncanonicalOrNonGlobalIPv4(t *testing.T) {
	t.Parallel()
	for _, address := range []string{
		"203.0.113.020", "203.0.113", "203.0.113.256", "0.0.0.0", "127.0.0.1",
		"169.254.1.1", "224.0.0.1", "255.255.255.255",
	} {
		address := address
		t.Run(address, func(t *testing.T) {
			t.Parallel()
			if _, err := Parse([]byte(command(address, "30m", false))); err == nil {
				t.Fatalf("address %q was accepted", address)
			}
		})
	}
	// IsGlobalUnicast deliberately does not implement M5-004 protected-range
	// policy. Documentation and private targets remain parseable ASTs, then are
	// blocked by the later protected-target gate outside this package.
	for _, address := range []string{"203.0.113.20", "10.0.0.1"} {
		if _, err := Parse([]byte(command(address, "30m", false))); err != nil {
			t.Fatalf("syntax-stage Parse(%q) error = %v", address, err)
		}
	}
}

func TestTTLCheckedBounds(t *testing.T) {
	t.Parallel()
	accepted := map[string]uint32{"1m": 60, "60s": 60, "1440m": 86400, "24h": 86400, "86400s": 86400}
	for input, want := range accepted {
		got, err := ParseTTL(input)
		if err != nil || got != want {
			t.Errorf("ParseTTL(%q) = %d, %v; want %d", input, got, err, want)
		}
	}
	for _, input := range []string{"0s", "59s", "86401s", "1441m", "25h", "99999h", "18446744073709551615h", "+60s", "-60s"} {
		if _, err := ParseTTL(input); err == nil {
			t.Errorf("ParseTTL(%q) succeeded", input)
		}
	}
	for _, seconds := range []uint32{0, 59, 86401, ^uint32(0)} {
		if _, err := CanonicalTTL(seconds); err == nil {
			t.Errorf("CanonicalTTL(%d) succeeded", seconds)
		}
	}
}

func TestBuildArtifactConsistencyFailures(t *testing.T) {
	t.Parallel()
	tests := []struct {
		name   string
		mutate func(*Policy, *Candidate)
		code   ErrorCode
	}{
		{"policy schema", func(p *Policy, _ *Candidate) { p.SchemaVersion = "response-policy-v2" }, ErrorSchema},
		{"candidate schema", func(_ *Policy, c *Candidate) { c.SchemaVersion = "shell-v1" }, ErrorSchema},
		{"action", func(p *Policy, _ *Candidate) { p.Action = "allow_ip" }, ErrorAction},
		{"policy target", func(p *Policy, _ *Candidate) { p.TargetIPv4 = "203.0.113.21" }, ErrorTargetMismatch},
		{"candidate target", func(_ *Policy, c *Candidate) { c.TargetIPv4 = "203.0.113.21" }, ErrorTargetMismatch},
		{"ttl token", func(_ *Policy, c *Candidate) { c.TimeoutToken = "1800s" }, ErrorCandidateMismatch},
		{"policy ttl", func(p *Policy, _ *Candidate) { p.TTLSeconds = 60 }, ErrorTTLMismatch},
		{"missing evidence", func(p *Policy, c *Candidate) { p.EvidenceIDs = nil; c.EvidenceIDs = nil }, ErrorEvidence},
		{"candidate evidence", func(_ *Policy, c *Candidate) { c.EvidenceIDs = []string{testEvidenceID(2)} }, ErrorEvidence},
		{"duplicate evidence", func(p *Policy, c *Candidate) {
			ids := []string{testEvidenceID(1), testEvidenceID(1)}
			p.EvidenceIDs = ids
			c.EvidenceIDs = ids
		}, ErrorEvidence},
		{"unordered evidence", func(p *Policy, c *Candidate) {
			ids := []string{testEvidenceID(2), testEvidenceID(1)}
			p.EvidenceIDs = ids
			c.EvidenceIDs = ids
		}, ErrorEvidence},
		{"invalid evidence", func(p *Policy, c *Candidate) { ids := []string{"not-a-uuid"}; p.EvidenceIDs = ids; c.EvidenceIDs = ids }, ErrorEvidence},
	}
	for _, test := range tests {
		test := test
		t.Run(test.name, func(t *testing.T) {
			t.Parallel()
			policy, candidate := validPair("30m", 1800)
			test.mutate(&policy, &candidate)
			_, err := BuildArtifact(policy, candidate)
			assertErrorCode(t, err, test.code)
		})
	}
}

func TestArtifactAccessorsAreDefensive(t *testing.T) {
	t.Parallel()
	policy, candidate := validPair("30m", 1800)
	artifact, err := BuildArtifact(policy, candidate)
	if err != nil {
		t.Fatal(err)
	}
	generated := artifact.GeneratedBytes()
	canonical := artifact.CanonicalBytes()
	evidence := artifact.EvidenceIDs()
	generated[0] = 'x'
	canonical[0] = 'x'
	evidence[0] = testEvidenceID(9)
	if artifact.GeneratedBytes()[0] != 'a' || artifact.CanonicalBytes()[0] != 'a' || artifact.EvidenceIDs()[0] != testEvidenceID(1) {
		t.Fatal("artifact state was mutable through an accessor")
	}
	candidate.GeneratedBytes[0] = 'x'
	policy.EvidenceIDs[0] = testEvidenceID(9)
	if artifact.GeneratedBytes()[0] != 'a' || artifact.EvidenceIDs()[0] != testEvidenceID(1) {
		t.Fatal("artifact retained caller-owned slices")
	}
}

func TestErrorsDoNotEchoGeneratedBytes(t *testing.T) {
	t.Parallel()
	const secretShapedInput = "add element inet sentinelflow blacklist_ipv4 { $(super-secret) timeout 30m }"
	_, err := Parse([]byte(secretShapedInput))
	if err == nil || strings.Contains(err.Error(), secretShapedInput) || strings.Contains(err.Error(), "super-secret") {
		t.Fatalf("unsafe parser error: %v", err)
	}
	var typed *Error
	if !errors.As(err, &typed) || typed.Code != ErrorSyntax {
		t.Fatalf("error = %T %v", err, err)
	}
}

func TestParseAcceptsNoLFOrExactlyOneLF(t *testing.T) {
	t.Parallel()
	base := command("203.0.113.20", "30m", false)
	for _, input := range []string{base, base + "\n"} {
		ast, err := Parse([]byte(input))
		if err != nil {
			t.Fatalf("Parse(%q) error = %v", input, err)
		}
		canonical, err := CanonicalBytes(ast)
		if err != nil {
			t.Fatal(err)
		}
		if string(canonical) != base+"\n" {
			t.Fatalf("canonical = %q", canonical)
		}
	}
}

type contractVectors struct {
	ArtifactDigests struct {
		NFTAdd string `json:"nft_add_artifact_sha256"`
	} `json:"artifact_digests"`
	Vectors struct {
		TTL struct {
			VectorName string `json:"vector_name"`
			Accepted   []struct {
				Input     string `json:"input"`
				Seconds   uint32 `json:"seconds"`
				Canonical string `json:"canonical"`
			} `json:"accepted"`
			Rejected []string `json:"rejected"`
		} `json:"ttl_canonical_v1"`
	} `json:"vectors"`
}

func loadVectors(t *testing.T) contractVectors {
	t.Helper()
	path := filepath.Join("..", "..", "contracts", "vectors", "contract_vectors_v1.json")
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read checked vectors: %v", err)
	}
	var vectors contractVectors
	if err := json.Unmarshal(data, &vectors); err != nil {
		t.Fatalf("decode checked vectors: %v", err)
	}
	if vectors.ArtifactDigests.NFTAdd == "" || len(vectors.Vectors.TTL.Accepted) == 0 || len(vectors.Vectors.TTL.Rejected) == 0 {
		t.Fatal("checked nft/TTL vectors are incomplete")
	}
	return vectors
}

func validPair(token string, seconds uint32) (Policy, Candidate) {
	generated := []byte(command("203.0.113.20", token, false))
	ids := []string{testEvidenceID(1), testEvidenceID(2)}
	return Policy{
			SchemaVersion: PolicySchemaVersion,
			Action:        ActionBlockIP,
			TargetIPv4:    "203.0.113.20",
			TTLSeconds:    seconds,
			EvidenceIDs:   append([]string(nil), ids...),
		}, Candidate{
			SchemaVersion:  CandidateSchemaVersion,
			TargetIPv4:     "203.0.113.20",
			TimeoutToken:   token,
			EvidenceIDs:    append([]string(nil), ids...),
			GeneratedBytes: generated,
		}
}

func command(address, ttl string, newline bool) string {
	result := "add element inet sentinelflow blacklist_ipv4 { " + address + " timeout " + ttl + " }"
	if newline {
		return result + "\n"
	}
	return result
}

func testEvidenceID(number int) string {
	return fmt.Sprintf("00000000-0000-0000-0000-%012x", number)
}

func assertErrorCode(t *testing.T, err error, code ErrorCode) {
	t.Helper()
	if err == nil {
		t.Fatalf("error = nil, want %s", code)
	}
	var typed *Error
	if !errors.As(err, &typed) || typed.Code != code {
		t.Fatalf("error = %T %v, want code %s", err, err, code)
	}
}
