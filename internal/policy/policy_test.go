func TestCheckedTTLCanonicalVectors(t *testing.T) {
	t.Parallel()
	vectors := loadVectors(t)
	if vectors.Vectors.TTL.VectorName != "ttl-canonical-v1" {
		t.Fatalf("vector name = %q", vectors.Vectors.TTL.VectorName)
	}
	for _, test := range vectors.Vectors.TTL.Accepted {
		test := test
		t.Run("accepted_"+test.Input, func(t *testing.T) {
			t.Parallel()
			seconds, err := ParseTTL(test.Input)
			if err != nil {
				t.Fatalf("ParseTTL(%q) error = %v", test.Input, err)
			}
			if seconds != test.Seconds {
				t.Fatalf("seconds = %d, want %d", seconds, test.Seconds)
			}
			canonical, err := CanonicalTTL(seconds)
			if err != nil {
				t.Fatalf("CanonicalTTL(%d) error = %v", seconds, err)
			}
			if canonical != test.Canonical {
				t.Fatalf("canonical = %q, want %q", canonical, test.Canonical)
			}
		})
	}
	for _, input := range vectors.Vectors.TTL.Rejected {
		input := input
		t.Run("rejected_"+input, func(t *testing.T) {
			t.Parallel()
			if _, err := ParseTTL(input); err == nil {
				t.Fatalf("ParseTTL(%q) succeeded", input)
			}
		})
	}
}

func TestCheckedCanonicalAddArtifact(t *testing.T) {
	t.Parallel()
	policy, candidate := validPair("30m", 1800)
	artifact, err := BuildArtifact(policy, candidate)
	if err != nil {
		t.Fatalf("BuildArtifact() error = %v", err)
	}
	wantCanonical := []byte("add element inet sentinelflow blacklist_ipv4 { 203.0.113.20 timeout 30m }\n")
	if !bytes.Equal(artifact.CanonicalBytes(), wantCanonical) {
		t.Fatalf("canonical bytes = %q, want %q", artifact.CanonicalBytes(), wantCanonical)
	}
	vectors := loadVectors(t)
	if artifact.CanonicalDigest() != vectors.ArtifactDigests.NFTAdd {
		t.Fatalf("canonical digest = %s, checked vector = %s", artifact.CanonicalDigest(), vectors.ArtifactDigests.NFTAdd)
	}
	if artifact.GeneratedDigest() != "sha256:cdf58824141aa6d280b4a5c32c5b2ecc50db68e334b104e4861e6a6421de3303" {
		t.Fatalf("generated digest = %s", artifact.GeneratedDigest())
	}
	if artifact.GeneratedDigest() == artifact.CanonicalDigest() {
		t.Fatal("no-LF generated bytes must remain distinct from canonical bytes")
	}
	if artifact.CanonicalTTLToken() != "30m" || artifact.AST().TTLSeconds() != 1800 || artifact.AST().TargetIPv4() != "203.0.113.20" {
		t.Fatalf("unexpected AST/artifact: token=%s seconds=%d target=%s", artifact.CanonicalTTLToken(), artifact.AST().TTLSeconds(), artifact.AST().TargetIPv4())
	}
}

func TestGeneratedNoncanonicalTTLCanonicalizesWithoutMutation(t *testing.T) {
	t.Parallel()
	policy, candidate := validPair("1800s", 1800)
	original := bytes.Clone(candidate.GeneratedBytes)
	artifact, err := BuildArtifact(policy, candidate)
	if err != nil {
		t.Fatalf("BuildArtifact() error = %v", err)
	}
	if artifact.AST().InputTTLToken() != "1800s" || artifact.CanonicalTTLToken() != "30m" {
		t.Fatalf("tokens = generated %q canonical %q", artifact.AST().InputTTLToken(), artifact.CanonicalTTLToken())
	}
	if !bytes.Equal(artifact.GeneratedBytes(), original) || !bytes.Equal(candidate.GeneratedBytes, original) {
		t.Fatal("generated bytes were silently rewritten")
	}
	if got := string(artifact.CanonicalBytes()); got != "add element inet sentinelflow blacklist_ipv4 { 203.0.113.20 timeout 30m }\n" {
		t.Fatalf("canonical bytes = %q", got)
	}
}

func TestCanonicalSerializerLargestExactUnitAndOneLF(t *testing.T) {
	t.Parallel()
	tests := []struct {
		token string
		secs  uint32
		want  string
	}{
		{"60s", 60, "1m"},
		{"61s", 61, "61s"},
		{"120s", 120, "2m"},
		{"60m", 3600, "1h"},
		{"24h", 86400, "24h"},
	}
	for _, test := range tests {
		test := test
		t.Run(test.token, func(t *testing.T) {
			t.Parallel()
			ast, err := Parse([]byte(command("203.0.113.20", test.token, false)))
			if err != nil {
				t.Fatalf("Parse() error = %v", err)
			}
			if ast.TTLSeconds() != test.secs {
				t.Fatalf("seconds = %d, want %d", ast.TTLSeconds(), test.secs)
			}
			canonical, err := CanonicalBytes(ast)
			if err != nil {
				t.Fatalf("CanonicalBytes() error = %v", err)
			}
			if !bytes.HasSuffix(canonical, []byte(" timeout "+test.want+" }\n")) || bytes.HasSuffix(canonical, []byte("\n\n")) || bytes.Count(canonical, []byte("\n")) != 1 {
				t.Fatalf("canonical output = %q", canonical)
			}
		})
	}
}

